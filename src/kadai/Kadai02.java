package kadai;

public class Kadai02 {

	public static void main(String[] args) {
		int a = 30;
		int b = 20;

		if (a % b == 0) {
			System.out.println("整数値A:"+a);
			System.out.println("整数値B:"+b);
			System.out.println("BはAの約数です。");
		}else {
			System.out.println("整数値A:"+a);
			System.out.println("整数値B:"+b);
			System.out.println("BはAの約数ではありません。");
		}

	}

}
